def _render_prompt_taxonomy(
    *,
    title: str,
    taxonomy: Iterable[CategoryDefinition],
    parent_level: int,
    child_level: int,
    parent_label: str,
    child_label: str,
) -> str:
    categories = tuple(taxonomy)
    children_by_parent: dict[str | None, list[CategoryDefinition]] = {}
    for category in categories:
        children_by_parent.setdefault(category.parent_key, []).append(category)

    lines = [title, ""]
    for parent in [category for category in categories if category.level == parent_level]:
        lines.append(f"{parent_label} {parent.key} (es: {parent.display_labels['es']}):")
        children = [
            child for child in children_by_parent.get(parent.key, []) if child.level == child_level
        ]
        if children:
            lines.append(
                f"  {child_label}: " + ", ".join(_prompt_label(child) for child in children)
            )
    return "\n".join(lines)


def _prompt_label(category: CategoryDefinition) -> str:
    es_label = category.display_labels["es"]
    return f"{category.key} (es: {es_label})"

"""Canonical four-level category taxonomy.

Canonical keys are English PascalCase and are the stable API/database/prompt
identifiers. User-facing text lives in display labels. Prompts may emit only:

- L2 Business Type keys for transaction/store categories.
- L4 Category keys for line-item categories.

L1 Industry and L3 Family are deterministic parent/group levels for reporting.
"""

from __future__ import annotations

from dataclasses import dataclass
from typing import TYPE_CHECKING

if TYPE_CHECKING:
    from collections.abc import Iterable, Mapping


@dataclass(frozen=True)
class CategoryDefinition:
    key: str
    level: int
    parent_key: str | None
    display_labels: Mapping[str, str]
    is_sensitive: bool
    sort_order: int


def _category(
    key: str,
    level: int,
    parent_key: str | None,
    *,
    en: str,
    es: str,
    pt: str,
    is_sensitive: bool = False,
    sort_order: int,
) -> CategoryDefinition:
    return CategoryDefinition(
        key=key,
        level=level,
        parent_key=parent_key,
        display_labels={"en": en, "es": es, "pt": pt},
        is_sensitive=is_sensitive,
        sort_order=sort_order,
    )


